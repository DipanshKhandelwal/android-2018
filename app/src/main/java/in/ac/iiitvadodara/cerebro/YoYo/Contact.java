class Contact {
    private String name;
    private long phone;
    private String role;

    public String getName() {
        return name;
    }

    public long getPhone() {
        return phone;
    }

    public String getRole() {
        return role;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPhone(long phone) {
        this.phone = phone;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
